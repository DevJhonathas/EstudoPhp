<?php
// Operadores: =, +=, -=, /=, *=, %=;

// +=
    $a = 0;
    $a += 10; //$a = $a + 10
    echo  "a += 10 : " . $a . "<br>";

// -=
    $b = 0;
    $b -= 10; //$a = $a + 10
    echo "b -= 10 : " . $b . "<br>";

// /=
    $c = 0;
    $c /= 10; //$a = $a + 10
    echo "c /= 10 : " . $c . "<br>";

// %=
    $d = 0;
    $d %= 10; //$a = $a + 10
    echo "d %= 10 : " . $d . "<br>";
    
// *=
    $e = 0;
    $e *= 10; //$a = $a + 10
    echo "e *= 10 : " . $e . "<br>";
?>